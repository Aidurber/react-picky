Cypress.Commands.add('option', index => {
  return cy.get(`#multiselect-option-${index}`);
});
Cypress.Commands.add('selectAll', index => {
  return cy.get(`#multiselect-option-selectall`).click();
});

Cypress.Commands.add('togglePicky', (options = {}) => {
  return cy.get('.picky__input').click(options);
});
